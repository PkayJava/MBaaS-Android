public class Data {

    private String title;
    private String mediaMp3;
    private String mediaVideo;
    private String mediaImage;
    private String description;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getMediaMp3() {
        return mediaMp3;
    }

    public void setMediaMp3(String mediaMp3) {
        this.mediaMp3 = mediaMp3;
    }

    public String getMediaVideo() {
        return mediaVideo;
    }

    public void setMediaVideo(String mediaVideo) {
        this.mediaVideo = mediaVideo;
    }

    public String getMediaImage() {
        return mediaImage;
    }

    public void setMediaImage(String mediaImage) {
        this.mediaImage = mediaImage;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
